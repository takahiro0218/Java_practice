public class Lesson3d {
    public static void main(String[] args) {
        // SLスタジオへようこそ
        String str1 = "SLスタジオへようこそ";
        System.out.println(str1);

        // "SLスタジオ"へようこそ
        str1 = "\"SLスタジオ\"へようこそ";
        System.out.println(str1);

        // SLスタジオ
        // へようこそ
        str1 = "SLスタジオ\nへようこそ";
        System.out.println(str1);

        // SLスタジオ    へようこそ
        str1 = "SLスタジオ\tへようこそ";
        System.out.println(str1);
    }
}
