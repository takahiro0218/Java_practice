public class Fukusyu7 {
    public static void main(String[] args) {
        int number = 10;
        // 10加算
        number += 10;
        System.out.println(number);
        // 5減算
        number -= 5;
        System.out.println(number);
        // 30乗算
        number *= 30;
        System.out.println(number);
        // 2で徐算
        number /= 2;
        System.out.println(number);
        // 7で徐算した余り
        number %= 7;
        System.out.println(number);
    }
}